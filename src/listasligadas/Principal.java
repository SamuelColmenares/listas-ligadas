    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        String semestre = cbSemestre.getSelectedItem().toString();
        try{
        Estudiante nuevoEstudiante = new Estudiante(
                txtNombre.getText(),
                txtCarnet.getText(),
                Integer.parseInt(semestre),
                Integer.parseInt("".equals(txtCreditos.getText()) ? "0" : txtCreditos.getText()),
                Integer.parseInt("".equals(txtValCredito.getText()) ? "0" : txtValCredito.getText()),
                Double.parseDouble("".equals(txtDesc.getText()) ? "0" : txtDesc.getText()));

        if (listaEstudiantes == null) {
            listaEstudiantes = nuevoEstudiante;
        } else {
            Estudiante ligados = listaEstudiantes.getSigEstudiante();
            if (ligados == null) {
                listaEstudiantes.setSigEstudiante(nuevoEstudiante);
            } else {
                while (ligados.getSigEstudiante() != null) {
                    ligados = ligados.getSigEstudiante();
                }

                ligados.setSigEstudiante(nuevoEstudiante);
            }
        }

        limpiarTabla();
        pintarTabla();
        lblMasPaga.setText(carnetMasPaga());
        limpiarCampos();
        }
        catch(Exception e){
            System.out.println("Valores no validos!");
            JOptionPane.showMessageDialog(null, "Valores no validos!");
            limpiarCampos();
        }
    }//GEN-LAST:event_btnCrearActionPerformed

