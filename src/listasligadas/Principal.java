    /**
     * Limpia la tabla de estudiantes.
     */
    private void limpiarTabla() {
        modelo = (DefaultTableModel) tableEtudiantes.getModel();
        while (tableEtudiantes.getRowCount() > 0) {
            modelo.removeRow(0);
        }
    }

    /**
     * Pinta la tabla con estudiantes.
     */
    private void pintarTabla() {
        if (listaEstudiantes == null) {
            System.out.println("Lista ligada vacia, no hay estudiantes adicionados.");
            return;
        }

        modelo = (DefaultTableModel) tableEtudiantes.getModel();
        Estudiante ligados = listaEstudiantes.getSigEstudiante();
        modelo.addRow(adicionarEstudianteARow(listaEstudiantes));
        if (ligados != null) {
            while (ligados.getSigEstudiante() != null) {
                modelo.addRow(adicionarEstudianteARow(ligados));
                ligados = ligados.getSigEstudiante();
            }

            modelo.addRow(adicionarEstudianteARow(ligados));
        }

        tableEtudiantes.setModel(modelo);
    }

    /**
     * Genera row de objetos para adicionar en la tabla.
     *
     * @param estudiante Elemento a adicionar en la tabla.
     * @return Arregla row a adicionar en la tabla.
     */
    private Object[] adicionarEstudianteARow(Estudiante estudiante) {
        Object[] row = new Object[7];
        row[0] = estudiante.getCarnet();
        row[1] = estudiante.getNombre();
        row[2] = estudiante.getSemestre();
        row[3] = estudiante.getCreditos();
        row[4] = estudiante.getValorCredito();
        row[5] = estudiante.getDescuento();
        row[6] = estudiante.getTotalPagar();
        return row;
    }

    /**
     * Busca el estudiante que paga más y retorna su carnet.
     *
     * @return Carnet de estudiante que mas paga.
     */
    private String carnetMasPaga() {
        if (listaEstudiantes == null) {
            return "No hay Estudiantes en la lista :(";
        }

        Estudiante ligados = listaEstudiantes.getSigEstudiante();

        if (ligados == null) {
            return listaEstudiantes.getCarnet();
        }

        double totalMax = listaEstudiantes.getTotalPagar();
        String carnet = ligados.getCarnet();
        while (ligados.getSigEstudiante() != null) {
            if (ligados.getTotalPagar() > totalMax) {
                totalMax = ligados.getTotalPagar();
                carnet = ligados.getCarnet();
            }

            ligados = ligados.getSigEstudiante();
        }

        if (ligados.getTotalPagar() > totalMax) {
            carnet = ligados.getCarnet();
        }

        return carnet;
    }
    
